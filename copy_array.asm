; Копирование массива из 5 элементов
; Исходный массив по адресу 1000, целевой - по адресу 2000

; Инициализация регистров
LOAD_CONST 0, 1000    ; Адрес источника
LOAD_CONST 1, 2000    ; Адрес назначения
LOAD_CONST 2, 5       ; Количество элементов

; Цикл копирования (вручную развёрнутый)
READ_MEM 0, 3         ; Чтение элемента источника
WRITE_MEM 3, 1        ; Запись в назначение

; Увеличиваем адреса
LOAD_CONST 4, 1       ; Константа 1
; Увеличиваем адрес источника
READ_MEM 0, 5
ADD 5, 4, 5          ; source_addr += 1
WRITE_MEM 5, 0

; Увеличиваем адрес назначения
READ_MEM 1, 5
ADD 5, 4, 5          ; dest_addr += 1
WRITE_MEM 5, 1

; Уменьшаем счётчик
READ_MEM 2, 5
LOAD_CONST 6, 1
SUB 5, 6, 5          ; count -= 1
WRITE_MEM 5, 2

